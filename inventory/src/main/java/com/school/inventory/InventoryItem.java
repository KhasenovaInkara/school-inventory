package com.school.inventory;

import jakarta.persistence.*;
import lombok.Data;
import java.time.LocalDate;

/**
 * Сущность, представляющая единицу инвентарного учета (товар или имущество).
 * <p>
 * Класс отображается на таблицу "items" в реляционной базе данных.
 * Инкапсулирует данные о материальных ценностях, их количественном состоянии и атрибутах.
 * <p>
 * Реализация требований:
 * - №5 (Хранение данных и структура БД).
 * - №6 (Принципы объектно-ориентированного программирования).
 */
@Entity
@Data
@Table(name = "items")
public class InventoryItem {

    /**
     * Уникальный идентификатор инвентарной единицы (Primary Key).
     * Генерируется автоматически на стороне СУБД (стратегия IDENTITY).
     */
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    /**
     * Наименование предмета (например, "Парта ученическая", "Проектор").
     * Используется для поиска и отображения в реестре.
     */
    private String title;

    /**
     * Категория имущества (например, "Мебель", "Оргтехника").
     * Служит для логической группировки объектов учета.
     */
    private String category;

    /**
     * Текущий количественный остаток на складе.
     * <p>
     * Является ключевым полем для логики выдачи:
     * если значение > 0, предмет доступен для запроса.
     * При значении 0 статус предмета интерпретируется как "Нет в наличии".
     */
    private Integer quantity; 

    /**
     * Информация о материально ответственном лице (или последнем пользователе).
     * Поле может использоваться для отслеживания текущего держателя
     * в случае штучного учета уникальных предметов.
     */
    private String responsible;

    /**
     * Дата постановки имущества на учет.
     * Фиксируется автоматически в момент создания записи.
     */
    private LocalDate dateAdded;
}